import * as ROT from 'rot-js';

// Render the game
function startGameRender() {
    // document.body.appendChild(display.getContainer());
    const container = document.getElementById('ascii-container');
    if (container) {
        container.innerHTML = '';
        container.appendChild(display.getContainer());
    } else {
        console.error("Container element not found. Could not render game.");
    }
}

// Game configuration
const config = {
    width: 58,
    height: 37,
};

// Initialize ROT.js display
const display = new ROT.Display({
    width: config.width,
    height: config.height,
    fontSize: 18
});

// Game variables
ROT.RNG.setSeed(52123134);
const map = new Map();
const player = {x: 10, y: 10, char: '@'};
let gameStarted = false;

// Generate a simple random dungeon map
function generateMap() {
    const digger = new ROT.Map.Digger(config.width, config.height);
    digger.create((x, y, wall) => {
        map.set(`${x},${y}`, {
            walkable: !wall,
            description: wall ? 'Wall' : 'Floor',
            char: wall ? '#' : '.',
            fg: wall ? '#666': '#333',
            bg: '#000'
        });
    });
    startingPlayerPosition();
}

function startingPlayerPosition() {
    let foundIt = false;
    while (!foundIt) {
        const x = Math.floor(ROT.RNG.getUniform() * config.width);
        const y = Math.floor(ROT.RNG.getUniform() * config.height);
        if (map.has(`${x},${y}`)) {
            player.x = x;
            player.y = y;
            foundIt = true;
        }
    }
}

function drawMap() {
    display.clear();
    map.forEach((value, key) => {
        const [x, y] = key.split(',').map(Number);
        display.draw(x, y, value.char, value.fg, value.bg);

    });
    display.draw(player.x, player.y, player.char, '#0f0'); // Player
}

function movePlayer(dx, dy) {
    const newX = player.x + dx;
    const newY = player.y + dy;

    const movingTo = map.get(`${newX},${newY}`.toString());

    if (map.get(`${newX},${newY}`.toString()).walkable) {
        player.x = newX;
        player.y = newY;
    }
}

// Handle input
function handleInput(event) {
    switch (event.key) {
        case 'ArrowUp':
            movePlayer(0, -1);
            break;
        case 'ArrowDown':
            movePlayer(0, 1);
            break;
        case 'ArrowLeft':
            movePlayer(-1, 0);
            break;
        case 'ArrowRight':
            movePlayer(1, 0);
            break;
    }
    drawMap();
}

// Game initialization
export function init() {
    if (!gameStarted) {
        startGameRender();
        generateMap();
        drawMap();
        window.addEventListener('keydown', handleInput);
        gameStarted = true;
    }

}

